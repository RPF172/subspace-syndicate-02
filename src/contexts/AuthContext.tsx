
import React, { createContext, useContext, useEffect, useState } from 'react';
import { useNavigate } from 'react-router-dom';
import { supabase } from '@/integrations/supabase/client';
import type { Session, User } from '@supabase/supabase-js';
import { toast } from '@/hooks/use-toast';

type AuthContextType = {
  session: Session | null;
  user: User | null;
  loading: boolean;
  signUp: (email: string, password: string, userData: SignUpMetadata) => Promise<void>;
  signIn: (email: string, password: string) => Promise<void>;
  signOut: () => Promise<void>;
};

type SignUpMetadata = {
  username: string;
  birthday: string;
  user_role: 'user' | 'creator' | 'admin';
  orientation: 'straight' | 'gay' | 'bisexual' | 'other';
  location: string;
  visibility?: 'Public' | 'Private' | 'Friends';
};

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const AuthProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [session, setSession] = useState<Session | null>(null);
  const [user, setUser] = useState<User | null>(null);
  const [loading, setLoading] = useState(true);
  
  // Move the useNavigate hook inside individual components that need it
  // This prevents hooks from being conditionally called

  useEffect(() => {
    // Set up auth state listener FIRST
    const { data: { subscription } } = supabase.auth.onAuthStateChange((_event, session) => {
      setSession(session);
      setUser(session?.user ?? null);
      setLoading(false);
    });

    // THEN check for existing session
    supabase.auth.getSession().then(({ data: { session } }) => {
      setSession(session);
      setUser(session?.user ?? null);
      setLoading(false);
    });

    return () => {
      subscription.unsubscribe();
    };
  }, []);

  const signUp = async (email: string, password: string, userData: SignUpMetadata) => {
    try {
      setLoading(true);
      
      // Set visibility to Public if not provided
      const metadata = {
        ...userData,
        visibility: userData.visibility || 'Public'
      };
      
      const { data, error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          data: metadata,
        },
      });

      if (error) throw error;

      // Insert into user_profiles if user was created
      if (data?.user) {
        const { id, email: userEmail } = data.user;
        const { username, birthday, user_role, orientation, location, visibility } = metadata;
        const { error: profileError } = await supabase.from('user_profiles').insert([
          {
            user_id: id,
            email: userEmail,
            username,
            birthday,
            user_role,
            orientation,
            location,
            visibility: visibility || 'Public',
          },
        ]);
        if (profileError) {
          toast({
            title: "Profile creation error",
            description: profileError.message,
            variant: "destructive",
          });
        }
      }

      toast({
        title: "Registration successful!",
        description: "Please check your email to confirm your account.",
      });
      // Navigate in the component using this function
    } catch (error: any) {
      toast({
        title: "Error during sign up",
        description: error.message,
        variant: "destructive",
      });
    } finally {
      setLoading(false);
    }
  };

  const signIn = async (email: string, password: string) => {
    try {
      setLoading(true);
      const { error } = await supabase.auth.signInWithPassword({
        email,
        password,
      });

      if (error) throw error;
      // Navigate in the component using this function
    } catch (error: any) {
      toast({
        title: "Error signing in",
        description: error.message,
        variant: "destructive",
      });
    } finally {
      setLoading(false);
    }
  };

  const signOut = async () => {
    try {
      setLoading(true);
      const { error } = await supabase.auth.signOut();
      if (error) throw error;
      // Navigate in the component using this function
    } catch (error: any) {
      toast({
        title: "Error signing out",
        description: error.message,
        variant: "destructive",
      });
    } finally {
      setLoading(false);
    }
  };

  return (
    <AuthContext.Provider value={{ session, user, loading, signUp, signIn, signOut }}>
      {children}
    </AuthContext.Provider>
  );
};

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};
